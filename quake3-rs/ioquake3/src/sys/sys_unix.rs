/*
==============
Sys_AppendToExecBuffer
==============
*/

unsafe extern "C" fn Sys_AppendToExecBuffer(mut text: *const i8) {
    let mut size: crate::stddef_h::size_t = (::std::mem::size_of::<[i8; 1024]>())
        .wrapping_sub(execBufferPointer.wrapping_offset_from(execBuffer.as_mut_ptr()) as usize);
    let mut length: i32 = crate::stdlib::strlen(text).wrapping_add(1usize) as i32;
    if length as usize > size
        || execArgc as usize
            >= (::std::mem::size_of::<[*mut i8; 16]>())
                .wrapping_div(::std::mem::size_of::<*mut i8>())
    {
        return;
    }
    crate::src::qcommon::q_shared::Q_strncpyz(execBufferPointer, text, size as i32);
    let fresh11 = execArgc;
    execArgc = execArgc + 1;
    execArgv[fresh11 as usize] = execBufferPointer;
    execBufferPointer = execBufferPointer.offset(length as isize);
}
/*
==============
Sys_Exec
==============
*/

unsafe extern "C" fn Sys_Exec() -> i32 {
    let mut pid: crate::stdlib::pid_t = crate::stdlib::fork();
    if pid < 0 {
        return -(1i32);
    }
    if pid != 0 {
        // Parent
        let mut exitCode: i32 = 0;
        crate::stdlib::wait(&mut exitCode);
        return (exitCode & 0xff00i32) >> 8i32;
    } else {
        // Child
        crate::stdlib::execvp(execArgv[0], execArgv.as_mut_ptr() as *const *mut i8);
        // Failed to execute
        crate::stdlib::exit(-(1i32));
    };
}
