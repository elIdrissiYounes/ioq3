/*
==================
Sys_Sleep

Block execution for msec or until input is received.
==================
*/
#[no_mangle]

pub unsafe extern "C" fn Sys_Sleep(mut msec: i32) {
    if msec == 0 {
        return;
    }
    if stdinIsATTY as u64 != 0 {
        let mut fdset: crate::stdlib::fd_set = crate::stdlib::fd_set {
            __fds_bits: [0; 16],
        };
        let mut __d0: i32 = 0;
        let mut __d1: i32 = 0;
        let fresh5 = &mut __d0;
        let fresh6;
        let fresh7 = &mut __d1;
        let fresh8;
        let fresh9 = (::std::mem::size_of::<crate::stdlib::fd_set>())
            .wrapping_div(::std::mem::size_of::<crate::stdlib::__fd_mask>());
        let fresh10 =
            &mut *fdset.__fds_bits.as_mut_ptr().offset(0) as *mut crate::stdlib::__fd_mask;
        asm!("cld; rep; stosq" : "={cx}" (fresh6), "={di}" (fresh8) : "{ax}" (0), "0"
     (c2rust_asm_casts::AsmCast::cast_in(fresh5, fresh9)), "1"
     (c2rust_asm_casts::AsmCast::cast_in(fresh7, fresh10)) : "memory" :
     "volatile");
        c2rust_asm_casts::AsmCast::cast_out(fresh5, fresh9, fresh6);
        c2rust_asm_casts::AsmCast::cast_out(fresh7, fresh10, fresh8);
        fdset.__fds_bits
            [(0 / (8 * ::std::mem::size_of::<crate::stdlib::__fd_mask>() as i32)) as usize] |=
            ((1usize) << 0 % (8 * ::std::mem::size_of::<crate::stdlib::__fd_mask>() as i32))
                as crate::stdlib::__fd_mask;
        if msec < 0 {
            crate::stdlib::select(
                0i32 + 1i32,
                &mut fdset,
                0 as *mut crate::stdlib::fd_set,
                0 as *mut crate::stdlib::fd_set,
                0 as *mut crate::stdlib::timeval,
            );
        } else {
            let mut timeout: crate::stdlib::timeval = crate::stdlib::timeval {
                tv_sec: 0,
                tv_usec: 0,
            };
            timeout.tv_sec = (msec / 1000i32) as crate::stdlib::__time_t;
            timeout.tv_usec = (msec % 1000i32 * 1000) as crate::stdlib::__suseconds_t;
            crate::stdlib::select(
                0i32 + 1i32,
                &mut fdset,
                0 as *mut crate::stdlib::fd_set,
                0 as *mut crate::stdlib::fd_set,
                &mut timeout,
            );
        }
    } else {
        // With nothing to select() on, we can't wait indefinitely
        if msec < 0 {
            msec = 10
        }
        crate::stdlib::usleep((msec * 1000i32) as crate::stdlib::__useconds_t);
    };
}
