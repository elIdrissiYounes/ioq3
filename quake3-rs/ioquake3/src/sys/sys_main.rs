/*
=================
Sys_AnsiColorPrint

Transform Q3 colour codes to ANSI escape sequences
=================
*/
#[no_mangle]

pub unsafe extern "C" fn Sys_AnsiColorPrint(mut msg: *const i8) {
    static mut buffer: [i8; 4096] = [0; 4096];
    let mut length: i32 = 0;
    static mut q3ToAnsi: [i32; 8] = [30, 31, 32, 33, 34, 36, 35, 0];
    while *msg != 0 {
        if crate::src::qcommon::q_shared::Q_IsColorString(msg) != 0 || *msg as i32 == '\n' as i32 {
            // First empty the buffer
            if length > 0 {
                buffer[length as usize] = '\u{0}' as i8;
                crate::stdlib::fputs(buffer.as_mut_ptr(), crate::stdlib::stderr);
                length = 0
            }
            if *msg as i32 == '\n' as i32 {
                // Issue a reset and then the newline
                crate::stdlib::fputs(
                    b"\x1b[0m\n\x00" as *const u8 as *const i8,
                    crate::stdlib::stderr,
                );
                msg = msg.offset(1)
            } else {
                // Print the color code
                crate::src::qcommon::q_shared::Com_sprintf(
                    buffer.as_mut_ptr(),
                    ::std::mem::size_of::<[i8; 4096]>() as i32,
                    b"\x1b[%dm\x00" as *const u8 as *const i8,
                    q3ToAnsi[(*msg.offset(1isize) as i32 - '0' as i32 & 0x7i32) as usize],
                );
                crate::stdlib::fputs(buffer.as_mut_ptr(), crate::stdlib::stderr);
                msg = msg.offset(2)
            }
        } else {
            if length >= 4096 - 1 {
                break;
            }
            buffer[length as usize] = *msg;
            length += 1;
            msg = msg.offset(1)
        }
    }
    // Empty anything still left in the buffer
    if length > 0 {
        buffer[length as usize] = '\u{0}' as i8;
        crate::stdlib::fputs(buffer.as_mut_ptr(), crate::stdlib::stderr);
    };
}
