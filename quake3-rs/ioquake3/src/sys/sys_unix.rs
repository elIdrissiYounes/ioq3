/*
=================
Sys_DllExtension

Check if filename should be allowed to be loaded as a DLL.
=================
*/
#[no_mangle]

pub unsafe extern "C" fn Sys_DllExtension(
    mut name: *const i8,
) -> crate::src::qcommon::q_shared::qboolean {
    let mut p: *const i8 = 0 as *const i8;
    let mut c: i8 = 0;
    if crate::src::qcommon::q_shared::COM_CompareExtension(
        name,
        b".so\x00" as *const u8 as *const i8,
    ) as u64
        != 0
    {
        return crate::src::qcommon::q_shared::qtrue;
    }
    // Check for format of filename.so.1.2.3
    p = crate::stdlib::strstr(name, b".so.\x00" as *const u8 as *const i8);
    if !p.is_null() {
        p = p.offset(crate::stdlib::strlen(b".so\x00" as *const u8 as *const i8) as isize);
        // Check if .so is only followed for periods and numbers.
        while *p != 0 {
            c = *p;
            if *(*crate::stdlib::__ctype_b_loc()).offset(c as i32 as isize) as i32
                & crate::stdlib::_ISdigit as u16 as i32
                == 0
                && c as i32 != '.' as i32
            {
                return crate::src::qcommon::q_shared::qfalse;
            }
            p = p.offset(1)
        }
        // Don't allow filename to end in a period. file.so., file.so.0., etc
        if c as i32 != '.' as i32 {
            return crate::src::qcommon::q_shared::qtrue;
        }
    }
    return crate::src::qcommon::q_shared::qfalse;
}
