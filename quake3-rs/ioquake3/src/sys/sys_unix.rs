/*
==============
Sys_Dialog

Display a *nix dialog box
==============
*/
#[no_mangle]

pub unsafe extern "C" fn Sys_Dialog(
    mut type_0: crate::qcommon_h::dialogType_t,
    mut message: *const i8,
    mut title: *const i8,
) -> crate::qcommon_h::dialogResult_t {
    let mut session: *const i8 =
        crate::stdlib::getenv(b"DESKTOP_SESSION\x00" as *const u8 as *const i8);
    let mut tried: [crate::src::qcommon::q_shared::qboolean; 4] = [
        crate::src::qcommon::q_shared::qfalse,
        crate::src::qcommon::q_shared::qfalse,
        crate::src::qcommon::q_shared::qfalse,
        crate::src::qcommon::q_shared::qfalse,
    ];
    let mut commands: [dialogCommandBuilder_t; 4] = [None, None, None, None];
    let mut preferredCommandType: dialogCommandType_t = NONE;
    let mut i: i32 = 0;
    commands[ZENITY as usize] = Some(
        Sys_ZenityCommand
            as unsafe extern "C" fn(
                _: crate::qcommon_h::dialogType_t,
                _: *const i8,
                _: *const i8,
            ) -> (),
    );
    commands[KDIALOG as usize] = Some(
        Sys_KdialogCommand
            as unsafe extern "C" fn(
                _: crate::qcommon_h::dialogType_t,
                _: *const i8,
                _: *const i8,
            ) -> (),
    );
    commands[XMESSAGE as usize] = Some(
        Sys_XmessageCommand
            as unsafe extern "C" fn(
                _: crate::qcommon_h::dialogType_t,
                _: *const i8,
                _: *const i8,
            ) -> (),
    );
    // This may not be the best way
    if crate::src::qcommon::q_shared::Q_stricmp(session, b"gnome\x00" as *const u8 as *const i8)
        == 0
    {
        preferredCommandType = ZENITY
    } else if crate::src::qcommon::q_shared::Q_stricmp(
        session,
        b"kde\x00" as *const u8 as *const i8,
    ) == 0
    {
        preferredCommandType = KDIALOG
    }
    i = NONE as i32 + 1;
    while i < NUM_DIALOG_PROGRAMS as i32 {
        if !(preferredCommandType != NONE && preferredCommandType != i as u32) {
            if tried[i as usize] as u64 == 0 {
                let mut exitCode: i32 = 0;
                commands[i as usize].expect("non-null function pointer")(type_0, message, title);
                exitCode = Sys_Exec();
                if exitCode >= 0 {
                    match type_0 {
                        3 => {
                            return if exitCode != 0 {
                                crate::qcommon_h::DR_NO as i32
                            } else {
                                crate::qcommon_h::DR_YES as i32
                            }
                                as crate::qcommon_h::dialogResult_t
                        }
                        4 => {
                            return if exitCode != 0 {
                                crate::qcommon_h::DR_CANCEL as i32
                            } else {
                                crate::qcommon_h::DR_OK as i32
                            }
                                as crate::qcommon_h::dialogResult_t
                        }
                        _ => return crate::qcommon_h::DR_OK,
                    }
                }
                tried[i as usize] = crate::src::qcommon::q_shared::qtrue;
                // The preference failed, so start again in order
                if preferredCommandType != NONE {
                    preferredCommandType = NONE;
                    i = NONE as i32 + 1
                }
            }
        }
        i += 1
    }
    crate::src::qcommon::common::Com_DPrintf(
        b"^3WARNING: failed to show a dialog\n\x00" as *const u8 as *const i8,
    );
    return crate::qcommon_h::DR_OK;
}
