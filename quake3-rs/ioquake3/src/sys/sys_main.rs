/*
=================
Sys_LoadDll

First try to load library name from system library path,
from executable path, then fs_basepath.
=================
*/
#[no_mangle]

pub unsafe extern "C" fn Sys_LoadDll(
    mut name: *const i8,
    mut useSystemLib: crate::src::qcommon::q_shared::qboolean,
) -> *mut libc::c_void {
    let mut dllhandle: *mut libc::c_void = 0 as *mut libc::c_void;
    if crate::src::sys::sys_unix::Sys_DllExtension(name) as u64 == 0 {
        crate::src::qcommon::common::Com_Printf(
            b"Refusing to attempt to load library \"%s\": Extension not allowed.\n\x00" as *const u8
                as *const i8,
            name,
        );
        return 0 as *mut libc::c_void;
    }
    if useSystemLib as u64 != 0 {
        crate::src::qcommon::common::Com_Printf(
            b"Trying to load \"%s\"...\n\x00" as *const u8 as *const i8,
            name,
        );
        dllhandle = crate::stdlib::SDL_LoadObject(name)
    }
    if dllhandle.is_null() {
        let mut topDir: *const i8 = 0 as *const i8;
        let mut libPath: [i8; 4096] = [0; 4096];
        let mut len: i32 = 0;
        topDir = Sys_BinaryPath();
        if *topDir == 0 {
            topDir = b".\x00" as *const u8 as *const i8
        }
        len = crate::src::qcommon::q_shared::Com_sprintf(
            libPath.as_mut_ptr(),
            ::std::mem::size_of::<[i8; 4096]>() as i32,
            b"%s%c%s\x00" as *const u8 as *const i8,
            topDir,
            '/' as i32,
            name,
        );
        if (len as usize) < ::std::mem::size_of::<[i8; 4096]>() {
            crate::src::qcommon::common::Com_Printf(
                b"Trying to load \"%s\" from \"%s\"...\n\x00" as *const u8 as *const i8,
                name,
                topDir,
            );
            dllhandle = crate::stdlib::SDL_LoadObject(libPath.as_mut_ptr())
        } else {
            crate::src::qcommon::common::Com_Printf(
                b"Skipping trying to load \"%s\" from \"%s\", file name is too long.\n\x00"
                    as *const u8 as *const i8,
                name,
                topDir,
            );
        }
        if dllhandle.is_null() {
            let mut basePath: *const i8 = crate::src::qcommon::cvar::Cvar_VariableString(
                b"fs_basepath\x00" as *const u8 as *const i8,
            );
            if basePath.is_null() || *basePath == 0 {
                basePath = b".\x00" as *const u8 as *const i8
            }
            if crate::src::qcommon::files::FS_FilenameCompare(topDir, basePath) as u64 != 0 {
                len = crate::src::qcommon::q_shared::Com_sprintf(
                    libPath.as_mut_ptr(),
                    ::std::mem::size_of::<[i8; 4096]>() as i32,
                    b"%s%c%s\x00" as *const u8 as *const i8,
                    basePath,
                    '/' as i32,
                    name,
                );
                if (len as usize) < ::std::mem::size_of::<[i8; 4096]>() {
                    crate::src::qcommon::common::Com_Printf(
                        b"Trying to load \"%s\" from \"%s\"...\n\x00" as *const u8 as *const i8,
                        name,
                        basePath,
                    );
                    dllhandle = crate::stdlib::SDL_LoadObject(libPath.as_mut_ptr())
                } else {
                    crate::src::qcommon::common::Com_Printf(
                        b"Skipping trying to load \"%s\" from \"%s\", file name is too long.\n\x00"
                            as *const u8 as *const i8,
                        name,
                        basePath,
                    );
                }
            }
            if dllhandle.is_null() {
                crate::src::qcommon::common::Com_Printf(
                    b"Loading \"%s\" failed\n\x00" as *const u8 as *const i8,
                    name,
                );
            }
        }
    }
    return dllhandle;
}
/*
=================
Sys_LoadGameDll

Used to load a development dll instead of a virtual machine
=================
*/
#[no_mangle]

pub unsafe extern "C" fn Sys_LoadGameDll(
    mut name: *const i8,
    mut entryPoint: *mut Option<unsafe extern "C" fn(_: i32, _: ...) -> crate::stdlib::intptr_t>,
    mut systemcalls: Option<
        unsafe extern "C" fn(_: crate::stdlib::intptr_t, _: ...) -> crate::stdlib::intptr_t,
    >,
) -> *mut libc::c_void {
    let mut libHandle: *mut libc::c_void = 0 as *mut libc::c_void;
    let mut dllEntry: Option<
        unsafe extern "C" fn(
            _: Option<
                unsafe extern "C" fn(_: crate::stdlib::intptr_t, _: ...) -> crate::stdlib::intptr_t,
            >,
        ) -> (),
    > = None;
    if crate::src::sys::sys_unix::Sys_DllExtension(name) as u64 == 0 {
        crate::src::qcommon::common::Com_Printf(
            b"Refusing to attempt to load library \"%s\": Extension not allowed.\n\x00" as *const u8
                as *const i8,
            name,
        );
        return 0 as *mut libc::c_void;
    }
    crate::src::qcommon::common::Com_Printf(
        b"Loading DLL file: %s\n\x00" as *const u8 as *const i8,
        name,
    );
    libHandle = crate::stdlib::SDL_LoadObject(name);
    if libHandle.is_null() {
        crate::src::qcommon::common::Com_Printf(
            b"Sys_LoadGameDll(%s) failed:\n\"%s\"\n\x00" as *const u8 as *const i8,
            name,
            crate::stdlib::SDL_GetError(),
        );
        return 0 as *mut libc::c_void;
    }
    dllEntry = ::std::mem::transmute::<
        *mut libc::c_void,
        Option<
            unsafe extern "C" fn(
                _: Option<
                    unsafe extern "C" fn(
                        _: crate::stdlib::intptr_t,
                        _: ...
                    ) -> crate::stdlib::intptr_t,
                >,
            ) -> (),
        >,
    >(crate::stdlib::SDL_LoadFunction(
        libHandle,
        b"dllEntry\x00" as *const u8 as *const i8,
    ));
    *entryPoint = ::std::mem::transmute::<
        *mut libc::c_void,
        Option<unsafe extern "C" fn(_: i32, _: ...) -> crate::stdlib::intptr_t>,
    >(crate::stdlib::SDL_LoadFunction(
        libHandle,
        b"vmMain\x00" as *const u8 as *const i8,
    ));
    if (*entryPoint).is_none() || dllEntry.is_none() {
        crate::src::qcommon::common::Com_Printf(
            b"Sys_LoadGameDll(%s) failed to find vmMain function:\n\"%s\" !\n\x00" as *const u8
                as *const i8,
            name,
            crate::stdlib::SDL_GetError(),
        );
        crate::stdlib::SDL_UnloadObject(libHandle);
        return 0 as *mut libc::c_void;
    }
    crate::src::qcommon::common::Com_Printf(
        b"Sys_LoadGameDll(%s) found vmMain function at %p\n\x00" as *const u8 as *const i8,
        name,
        *entryPoint,
    );
    dllEntry.expect("non-null function pointer")(systemcalls);
    return libHandle;
}
