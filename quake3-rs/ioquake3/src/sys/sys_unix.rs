#[no_mangle]

pub unsafe extern "C" fn Sys_ErrorDialog(mut error: *const i8) {
    let mut buffer: [i8; 1024] = [0; 1024];
    let mut size: u32 = 0;
    let mut f: i32 = -(1);
    let mut homepath: *const i8 = crate::src::qcommon::cvar::Cvar_VariableString(
        b"fs_homepath\x00" as *const u8 as *const i8,
    );
    let mut gamedir: *const i8 =
        crate::src::qcommon::cvar::Cvar_VariableString(b"fs_game\x00" as *const u8 as *const i8);
    let mut fileName: *const i8 = b"crashlog.txt\x00" as *const u8 as *const i8;
    let mut dirpath: *mut i8 = crate::src::qcommon::files::FS_BuildOSPath(
        homepath,
        gamedir,
        b"\x00" as *const u8 as *const i8,
    );
    let mut ospath: *mut i8 =
        crate::src::qcommon::files::FS_BuildOSPath(homepath, gamedir, fileName);
    crate::src::sys::sys_main::Sys_Print(crate::src::qcommon::q_shared::va(
        b"%s\n\x00" as *const u8 as *mut i8,
        error,
    ));
    Sys_Dialog(
        crate::qcommon_h::DT_ERROR,
        crate::src::qcommon::q_shared::va(
            b"%s. See \"%s\" for details.\x00" as *const u8 as *mut i8,
            error,
            ospath,
        ),
        b"Error\x00" as *const u8 as *const i8,
    );
    // Make sure the write path for the crashlog exists...
    if Sys_Mkdir(homepath) as u64 == 0 {
        crate::src::qcommon::common::Com_Printf(
            b"ERROR: couldn\'t create path \'%s\' for crash log.\n\x00" as *const u8 as *const i8,
            homepath,
        );
        return;
    }
    if Sys_Mkdir(dirpath) as u64 == 0 {
        crate::src::qcommon::common::Com_Printf(
            b"ERROR: couldn\'t create path \'%s\' for crash log.\n\x00" as *const u8 as *const i8,
            dirpath,
        );
        return;
    }
    // We might be crashing because we maxed out the Quake MAX_FILE_HANDLES,
    // which will come through here, so we don't want to recurse forever by
    // calling FS_FOpenFileWrite()...use the Unix system APIs instead.
    f = crate::stdlib::open(ospath, 0o100 | 0o1000 | 0o1, 0o640i32);
    if f == -(1) {
        crate::src::qcommon::common::Com_Printf(
            b"ERROR: couldn\'t open %s\n\x00" as *const u8 as *const i8,
            fileName,
        );
        return;
    }
    loop
    // We're crashing, so we don't care much if write() or close() fails.
    {
        size = crate::src::sys::con_log::CON_LogRead(
            buffer.as_mut_ptr(),
            ::std::mem::size_of::<[i8; 1024]>() as u32,
        );
        if !(size > 0) {
            break;
        }
        if !(crate::stdlib::write(
            f,
            buffer.as_mut_ptr() as *const libc::c_void,
            size as crate::stddef_h::size_t,
        ) != size as isize)
        {
            continue;
        }
        crate::src::qcommon::common::Com_Printf(
            b"ERROR: couldn\'t fully write to %s\n\x00" as *const u8 as *const i8,
            fileName,
        );
        break;
    }
    crate::stdlib::close(f);
}

