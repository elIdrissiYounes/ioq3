/*
================
Sys_Milliseconds
================
*/
/* base time in seconds, that's our origin
timeval:tv_sec is an int:
assuming this wraps every 0x7fffffff - ~68 years since the Epoch (1970) - we're safe till 2038 */
#[no_mangle]

pub static mut sys_timeBase: usize = 0;
/* current time in ms, using sys_timeBase as origin
NOTE: sys_timeBase*1000 + curtime -> ms since the Epoch
  0x7fffffff ms - ~24 days
although timeval:tv_usec is an int, I'm not sure wether it is actually used as an unsigned int
  (which would affect the wrap period) */
#[no_mangle]

pub static mut curtime: i32 = 0;
#[no_mangle]

pub unsafe extern "C" fn Sys_Milliseconds() -> i32 {
    let mut tp: crate::stdlib::timeval = crate::stdlib::timeval {
        tv_sec: 0,
        tv_usec: 0,
    };
    crate::stdlib::gettimeofday(&mut tp, 0 as *mut crate::stdlib::timezone);
    if sys_timeBase == 0 {
        sys_timeBase = tp.tv_sec as usize;
        return (tp.tv_usec / 1000isize) as i32;
    }
    curtime = (tp.tv_sec as usize)
        .wrapping_sub(sys_timeBase)
        .wrapping_mul(1000usize)
        .wrapping_add((tp.tv_usec / 1000) as usize) as i32;
    return curtime;
}
/*
==================
Sys_RandomBytes
==================
*/
#[no_mangle]

pub unsafe extern "C" fn Sys_RandomBytes(
    mut string: *mut crate::src::qcommon::q_shared::byte,
    mut len: i32,
) -> crate::src::qcommon::q_shared::qboolean {
    let mut fp: *mut crate::stdlib::FILE = 0 as *mut crate::stdlib::FILE; // don't buffer reads from /dev/urandom
    fp = crate::stdlib::fopen(
        b"/dev/urandom\x00" as *const u8 as *const i8,
        b"r\x00" as *const u8 as *const i8,
    );
    if fp.is_null() {
        return crate::src::qcommon::q_shared::qfalse;
    }
    crate::stdlib::setvbuf(fp, 0 as *mut i8, 2, 0);
    if crate::stdlib::fread(
        string as *mut libc::c_void,
        ::std::mem::size_of::<crate::src::qcommon::q_shared::byte>(),
        len as usize,
        fp,
    ) != len as usize
    {
        crate::stdlib::fclose(fp);
        return crate::src::qcommon::q_shared::qfalse;
    }
    crate::stdlib::fclose(fp);
    return crate::src::qcommon::q_shared::qtrue;
}
