pub unsafe extern "C" fn MuLawEncode(mut s: i16) -> crate::src::qcommon::q_shared::byte {
    let mut adjusted: usize = 0;
    let mut sign: crate::src::qcommon::q_shared::byte = 0;
    let mut exponent: crate::src::qcommon::q_shared::byte = 0;
    let mut mantissa: crate::src::qcommon::q_shared::byte = 0;
    sign = if (s as i32) < 0 { 0i32 } else { 0x80 } as crate::src::qcommon::q_shared::byte;
    if (s as i32) < 0 {
        s = -(s as i32) as i16
    }
    adjusted = ((s as isize)
        << (16usize).wrapping_sub((::std::mem::size_of::<i16>()).wrapping_mul(8usize)))
        as usize;
    adjusted = adjusted.wrapping_add((128isize + 4) as usize);
    if adjusted > 32767 {
        adjusted = 32767
    }
    exponent = (numBits[adjusted >> 7 & 0xff] as i32 - 1) as crate::src::qcommon::q_shared::byte;
    mantissa = (adjusted >> exponent as i32 + 3 & 0xf) as crate::src::qcommon::q_shared::byte;
    return !(sign as i32 | (exponent as i32) << 4 | mantissa as i32)
        as crate::src::qcommon::q_shared::byte;
}
